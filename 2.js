const mappingScore = {
  A:1, // rock
  B:2, // paper
  C:3, // scissors
};

const mappingPairs = {
  X: 'A',
  Y: 'B',
  Z: 'C',
};

const WIN   = 6;
const DRAW  = 3;
const LOSE  = 0;

const mappingOutcome = {
  X: LOSE,
  Y: DRAW,
  Z: WIN,
}

const rules = (my, opp) => {
  if (my === opp) return DRAW;
  if (my === 'A') {
    if (opp === 'B') return LOSE;
    if (opp === 'C') return WIN;
  }

  if (my === 'B') {
    if (opp === 'A') return WIN;
    if (opp === 'C') return LOSE;
  }

  if (my === 'C') {
    if (opp === 'A') return LOSE;
    if (opp === 'B') return WIN;
  }
}

const game = (opp, enc) => {
  const my = mappingPairs[enc];
  return rules(my, opp) + mappingScore[my];
}


